Create database AMS;
USE AMS;

CREATE TABLE ROLE (
 ID BIGINT(20) NOT NULL AUTO_INCREMENT,
 NAME VARCHAR(255) NOT NULL,
PRIMARY KEY ( ID )
) ;
CREATE TABLE SKILL (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 DESCRIPTION VARCHAR(255) DEFAULT NULL,
 NAME VARCHAR(255) NOT NULL,
PRIMARY KEY ( ID )       /*bigint value removed*/
) ;
CREATE TABLE DEPARTMENT (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 NAME VARCHAR(255) NOT NULL,
PRIMARY KEY ( ID )
) ;
CREATE TABLE DEGREE (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 DEPARTMENT_ID BIGINT(20) NOT NULL,
 NAME VARCHAR(255) NOT NULL,
PRIMARY KEY ( ID ),
foreign key ( DEPARTMENT_ID ) references department (ID)
) ;
CREATE TABLE PROFILE (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 ADDRESS VARCHAR(255) NOT NULL,
 BATCH VARCHAR(255) NOT NULL,
 DEGREE_ID BIGINT NOT NULL,
 DESIGNATION VARCHAR(255) DEFAULT NULL,
 GENDER VARCHAR(255) NOT NULL,
PRIMARY KEY ( ID ),
foreign key ( DEGREE_ID ) references degree ( ID )
) ;
CREATE TABLE USER (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 EMAILID VARCHAR(255) NOT NULL,
 NAME VARCHAR(255) NOT NULL,
 PASSWORD VARCHAR(255) NOT NULL,
 PHONENUMBER VARCHAR(255) NOT NULL,
 PROFILE_ID BIGINT DEFAULT NULL,
 ROLE_ID BIGINT(20) NOT NULL,
 USERNAME VARCHAR(255) NOT NULL,
PRIMARY KEY ( ID ),
foreign KEY ( PROFILE_ID ) references PROFILE( ID ),
foreign KEY ( ROLE_ID ) references ROLE( ID )
) ;
CREATE TABLE EXPERIENCE (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 COMPANY_NAME VARCHAR(255) NOT NULL,
 CURRENT BIT(1) NOT NULL,
 END DATETIME DEFAULT NULL,
 START DATETIME NOT NULL,
 PROFILE_ID BIGINT NOT NULL,
PRIMARY KEY ( ID ),
foreign key ( PROFILE_ID )references PROFILE( ID )
) ;
CREATE TABLE PROJECT (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 NAME VARCHAR(255) NOT NULL,
 NUMBER_OF_MEMBERS INT(11) NOT NULL,
 PROFILE_ID BIGINT NOT NULL,
 SHORT_DESCRIPTION VARCHAR(255) DEFAULT NULL,
PRIMARY KEY ( ID ),
foreign key ( PROFILE_ID )references PROFILE( ID )
);
CREATE TABLE PROFILE_SKILLS (
 SKILL_ID BIGINT NOT NULL,
 PROFILE_ID BIGINT NOT NULL,
PRIMARY KEY ( PROFILE_ID , SKILL_ID ),
foreign key ( PROFILE_ID )references PROFILE( ID ),
foreign key ( Skill_id )references skill( ID )
) ;
CREATE TABLE HIGHER_DEGREE (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 DEGREE_NAME VARCHAR(255) DEFAULT NULL,
 UNIVERSITY_NAME VARCHAR(255) DEFAULT NULL,
 PROFILE_ID BIGINT NOT NULL,
PRIMARY KEY ( ID ),
foreign key ( PROFILE_ID )references PROFILE( ID )
) ;
CREATE TABLE EVENT (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 DATE DATETIME NOT NULL,
 DESCRIPTION VARCHAR(255) DEFAULT NULL,
 INVITATION VARCHAR(255) NOT NULL,
 NAME VARCHAR(255) NOT NULL,
 ORGANISER_ID BIGINT NOT NULL,
PRIMARY KEY ( ID ),
foreign KEY ( ORGANISER_ID ) references User ( ID )
);
CREATE TABLE QUERY (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 CONTENT VARCHAR(255) NOT NULL,
 DATE DATETIME NOT NULL,
 PARENT_ID BIGINT DEFAULT NULL,
 USER_ID BIGINT NOT NULL,
PRIMARY KEY ( ID ),
foreign key ( User_ID )references User( ID ),
foreign KEY (PARENT_ID) references query ( ID )
) ;
CREATE TABLE POST_TYPE (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 NAME VARCHAR(255) NOT NULL,
PRIMARY KEY ( ID )
) ;
CREATE TABLE POST (
 ID BIGINT NOT NULL AUTO_INCREMENT,
 CONTENT VARCHAR(255) NOT NULL,
 DATE DATETIME NOT NULL,
 TYPE_ID BIGINT NOT NULL,
 USER_ID BIGINT NOT NULL,
PRIMARY KEY ( ID ),
foreign key ( type_ID )references post_type( ID ),
foreign key ( User_ID )references User( ID )
) ;
show tables;

